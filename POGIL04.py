def do_output_format_demo():
    """
        Demonstrates using format.
    """
    print(
        "Output can be formatted to two decimal places with "
        "format(variableName, '.2f')")
    price = 599.9999
    print("For example, 599.999 becomes:", format(price, '.2f'))
